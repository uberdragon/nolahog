          <h1>Group Riding Tips: Before the Road</h1>
          <p>Whether it's as part of a large organized ride (as we describe in this article) or with a small group of friends heading out together to enjoy an afternoon, group riding presents special challenges – and offers unique rewards.
          </p>
          <p>
          In about 45 minutes a riders' meeting will begin, where everyone will be briefed on the plan for the day's ride. Kickstands go up at 9:30 sharp – but your planning for this ride actually began a few weeks ago.
          </p>
          <h2>Before the Ride</h2>
            <p>Before you signed up for the ride, the first thing you did was ask yourself if the demands of this particular ride are within your comfort zone. Is the distance one you can handle comfortably? Are the roads within your skill level? If you're not sure, talk to the ride organizers. Don't be afraid to ask questions – and don't feel pressured to take part in a ride you're not comfortable with.</p>
            <p>On the morning of the ride, you began your day by giving your bike a thorough pre-ride inspection. Then you selected the proper riding attire for the day. Even though it's expected to be sunny and warm, you packed some heavier clothes and raingear in your saddlebags – right next to your cell phone, first-aid kit, tool kit, and owner's manual – just in case.</p>
            <h2>Listen Up!</h2>
            <p>Back at the diner, as the breakfast dishes are being cleared away, the Ride Captain authoritatively taps a spoon on his water glass and officially calls the riders' meeting to order. After a few welcoming comments, he or she goes over the ground rules for the day and passes out a packet of information. This includes a map outlining the day's route, an itinerary detailing the planned stops, information about the availability of food and fuel along the way, and a cell phone contact number for emergencies. Knowing how important it is that everyone is on the same page before the ride begins, all the riders pay close attention.</p>
            <p>The Ride Captain advises riders to break into smaller groups of five to seven bikes each for the ride and discusses the plan for re-grouping should anyone become separated. Knowing ahead of time where to re-join the group will reduce the pressure to "make the light" or hurry through a difficult left turn.</p>
            <p>The Ride Captain concludes the meeting by going over the basic group riding hand signals, a useful tool to help communicate with other riders and keep the group together. Hand signals vary, so <a href="/group-riding-hand-signals.php">make sure you're familiar with the ones being used</a>. Some of the more common signals are shown below.</p>


