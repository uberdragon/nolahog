              <h1>New Orleans H.O.G. Chapter Information</h1><h2>How do I join?</h2>
              <p>You must first be a current National H.O.G. 
              member in good standing. If you are not a member, contact the H.O.G. office at  1-800-CLUB-HOG to 
              join or complete a <a title="National H.O.G. Membership Enrollment Form" href="https://members.hog.com/website/join/JoinHOGApply.jsp?Country=US" target="_blank">National membership application</a> on the <a class="outbound" title="National H.O.G. website" href="http://www.harley-davidson.com/wcm/Content/Pages/HOG/HOG.jsp?locale=en_US" target="_blank">National H.O.G. site</a>.&nbsp; If you are currently a member or had a prior membership (within the last two years) and wish to renew, contact the H.O.G. office at 1-800-CLUBHOG or log-in to the <a href="https://profile.harley-davidson.com/cpapp/auth/ILTicket.h-d?ret_uri=https%3A//members.hog.com/website/profile/InitSession.jsp&req_uri=https%3A//members.hog.com/website/main.jsp" target="_blank">Members Only</a> site to renew on-line. 
              </p>
              <p>Your spouse or 
              children are welcome as associate members; however, you must add them as 
              associates to your National membership first. Each member, full or associate, 
              will receive their own membership number from National H.O.G. along with 
              a package of information, pins, and patches. Please provide our local chapter 
              with your National membership number(s) at the time you enroll, or if you 
              are a new National member, as soon as you receive your number from National.</p>     
              <p>Once you are a National H.O.G. member, take a trip down to the <a class="outbound" title="Get Directions to New Orleans Harley-Davidson" href="http://maps.google.com/?daddr=New Orleans Harley-Davidson, Metairie, LA">Harley-Davidson 
              of New Orleans</a> dealership for an enrollment form.</p>
              <p>For your convenience, click <a class="outbound" title="Complete and Print the Enrollment Form" href="enroll.php" target="_blank">here</a> 
              to print the form online. Fill in all needed information and take it down 
              to the dealership with you to pay your membership fees.</p>
              
              <h2>When are the meetings held?</h2>
              <p>Our Chapter usually 
              meets the first Saturday of each month. Meetings start at 10 a.m. and typically 
              lasts an hour.  We always recommend checking the <a href="/calendar.php">Online Calendar</a> to verify meetings and events.</p>
              
              <h2>Where are the meetings held?</h2>
              <p><a class="outbound" title="Get Directions to New Orleans Harley-Davidson" href="http://maps.google.com/?daddr=New Orleans Harley-Davidson, Metairie, LA">New Orleans Harley Davidson on Airline Drive</a> in Metairie Louisiana.</p>

              <h2>How much are the dues?</h2>
              <p>Local chapter dues are $24.00 per year and are due July 1st.</p>
              
              <h2>Is there a newsletter?</h2>
              <p>Yes! Our chapter newsletter, 
                <a href="/newsletter.php">The HOGPEN</a>, is full of information about the 
                current goings on and recent history of chapter activities. In addition, 
                there is a calendar of upcoming events that keeps us informed on what's 
                planned. Our newsletter is something we take great pride in, and we urge 
                all members to contribute to its content. We're always looking for one more 
                article or picture to put in it, whether it's an article of fiction, or 
                letting us know about your most recent weekend trip.</p>
              <p>Submissions are due by the 10th of every month in order for the Editor to 
                get them in the next month's newsletter.</p>
              <p>If you would like to receive the newsletter via email, click 
		            <a href="/hogpenRequest.php">here</a>.</p>
		            
		          <h2>Donation/Donation</h2>
		          <p>Our Chapter takes donations 
                at each meeting. A $5.00 donation will get 1 ticket. At the end of the meeting 
                one ticket is drawn and the person holding that ticket receives ½ of the 
                ticket sales. The other ½ goes to the Club to help with events & other expenses. 
                Other tickets are drawn for various door prizes donated by our sponsors.</p>
                
