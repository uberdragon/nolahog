            <h1>Officer Responsibilities</h1> 
            <font face="arial, Arial, Helvetica">
            <p ><strong><font color="#D24400" >Officers assist the sponsoring dealer and chapter director in 
            upholding the &#147;Annual Charter for H.O.G. Chapters&#148;</font></strong></p> 
 
            <h2><a href="chapter-officers.php#director" name="director" style="color:#000;">Director</a></h2> 
              <p>
                <ul>
                  <li>Assumes overall responsibility for the administration of the H.O.G. chapter and implementation of Harley Owners Group policies</li>
                  <li>Signs annual H.O.G. Chapter Charter Application and ensures it is received in H.O.G. office by January 31</li>
                  <li>Promotes H.O.G. and the H.O.G. chapter to potential members</li>
                  <li>Conducts chapter meetings</li>
                  <li>Coordinates chapter officer responsibilities</li>
                  <li>Ensures that risk management requirements set forth by the Chapter Insurance program, including procurement of necessary release forms, are met for all chapter activities</li>
                  <li>Verifies that chapter T-shirt guidelines are followed</li>
                  <li>Ensures that all trademark requirements are met</li>
                  <li>Ensures that chapter merchandise orders are supplied to the sponsoring dealer</li>
                  <li>Ensures Chapter Officer News® is available to all fellow officers</li>
                  <li>Routes H.O.G. correspondence to fellow officers and to membership, as requested</li>
                </ul>
              </p>
            <h2><a href="chapter-officers.php#asstdirector" name="asstdirector" style="color:#000;">Assistant Director</a></h2> 
              <p>
                <ul>
                  <li>Assists the director as requested</li>
                  <li>Promotes H.O.G. and the H.O.G. chapter to potential members</li>
                  <li>Informs chapter members of H.O.G. programs and benefits.</li>
                  <li>Conducts new member orientation</li>
                  <li>Acts as the liaison between the chapter and the State Rally Committee</li>
                </ul>
              </p>
            <h2><a href="chapter-officers.php#treasurer" name="treasurer" style="color:#000;">Treasurer</a></h2> 
              <p>
                <ul>
                  <li>Collects chapter dues (no more than $24 per member per year)</li>
                  <li>Manages all chapter funds</li>
                  <li>Oversees the preparation of chapter federal, state and local tax returns</li>
                  <li>Reports financial transactions to members monthly</li>
                  <li>Submits the annual financial statement to H.O.G. (by January 31)</li>
                  <li>Ensures that any charitable contributions are managed according to H.O.G. guidelines</li>
                  <li>Maintains a running comparison of current year chapter finances with prior year chapter finances for presentation at Annual Meeting</li>
                </ul>
              </p>
            <h2><a href="chapter-officers.php#secretary" name="secretary" style="color:#000;">Secretary</a></h2> 
              <p>
                <ul>
                  <li>Oversees the administrative needs of the chapter</li>
                  <li>Keeps minutes of all chapter meetings, including “Annual Chapter Business Meeting”</li>
                  <li>Ensures that all insurance requirements are met</li>
                  <li>Ensure that chapter members have current national H.O.G. memberships</li>
                  <li>Enroll new chapter members</li>
                  <li>Administers annual chapter member renewal process</li>
                  <li>Ensure that the most current officer and chapter address information is on file with H.O.G.</li>
                  <li>Maintain annual Chapter Membership Enrollment Form and Release on file for each member</li>
                  <li>Submits the H.O.G. Chapter Charter Application (once a year, by January 31)</li>
                  <li>Obtains, files and maintains records of all insurance forms, including release and injury report forms</li>
                  <li>Administers the annual chapter business meeting</li>
                </ul>
              </p>
            <h2><a href="chapter-officers.php#activities" name="activities" style="color:#000;">Activities Officer</a></h2> 
              <p>
                <ul>
                  <li>Oversees the administration of chapter events</li>
                  <li>Recruits and instructs volunteers for chapter activities</li>
                  <li>Survey chapter members on the types of activities that interest them, and ask if they are willing to volunteer. If and when those activities are held, notify the members who showed a special interest</li>
                  <li>Ensures that all insurance concerns and requirements are met for chapter events</li>
                  <li>Oversees the collection of appropriate release forms for chapter events</li>
                  <li>Assists the director and Secretary with the implementation of the Risk Management program, including procurement of insurance and release Forms</li>
                </ul>
              </p>
            <h2><a href="chapter-officers.php#loh" name="loh" style="color:#000;">Ladies of Harley® Officer (LOH)</a></h2> 
              <p>
                <ul>
                  <li>Encourages women members to take an active part in the chapter</li>
                  <li>Answers questions about the Ladies of Harley program</li>
                  <li>Informs members of LOH benefits and activities</li>
                  <li>Coordinates LOH chapter activities</li>
                </ul>
              <p><strong>Remember</strong>: Ladies of Harley is another benefit of membership, not a separate organization within H.O.G. or local chapters. 
              Ladies of Harley was established to help women become more active members of H.O.G. and their existing local chapter. You cannot open a separate Ladies of Harley chapter nor offer activities
              exclusive to LOH members. You can, of course, organize events that may be more appealing to the LOH members, but all members must be welcome.</p>
              </p>
            <h2><a href="chapter-officers.php#roadcaptain" name="roadcaptain" style="color:#000;">Road Captain</a></h2> 
              <p>
                <ul>
                  <li>Assists in planning routes for chapter rides</li>
                  <li>Assists in keeping the chapter informed of all H.O.G. programs</li>
                  <li>Educates chapter members about group riding techniques</li>
                  <li>Informs chapter members of any hand signals used by the chapter on group rides</li>
                  <li>Assists in obtaining signed release forms for chapter rides</li>
                  <li>Acts as a guide for organized chapter rides</li>
                </ul>
              </p>
            <h2><a href="chapter-officers.php#editor" name="editor" style="color:#000;">Editor</a></h2> 
              <p>
                <ul>
                  <li>Authors, edits and facilitates the distribution of chapter publications, including the chapter newsletter</li>
                  <li>Presents any chapter publications (written, electronic, oral, etc.) to the sponsoring dealer for approval prior to their use</li>
                  <li>Organizes and assembles written material for the chapter</li>
                  <li>Submits chapter events on-line at members.hog.com for listing on the Chapter Events List</li>
                  <li>Submits chapter suggestions to the Chapter Officer News ®</li>
                  <li>Informs chapter members of chapter activities by printing them in the Chapter newsletter</li>
                  <li>Work with Chapter Webmaster to ensure the same information is being communicated to all chapter members.</li>
                  <li>Promotes H.O.G.’s fun, family-oriented philosophy in all chapter publications</li>
                  <li>Sends a copy of all publications to the H.O.G. office</li>
                </ul>
              </p>
            <h2><a href="chapter-officers.php#safety" name="safety" style="color:#000;">Safety Officer</a></h2> 
              <p>
                <ul>
                  <li>Provides Motorcycle Safety Foundation information to members</li>
                  <li>Educates members about the Safe Rider Skills program</li>
                  <li>Assists the Road Captain in planning routes for chapter rides</li>
                  <li>Assists the Road Captain in educating members about group riding techniques</li>
                  <li>Assists the Road Captain in informing chapter members of any hand signals used by the chapter</li>
                  <li>Assists in the collection of signed release forms</li>
                </ul>
              </p>
            <h2><a href="chapter-officers.php#photographer" name="photographer" style="color:#000;">Photographer</a></h2> 
              <p>
                <ul>
                  <li>Takes and organizes chapter photos for chapter publications, Hog calls and a chapter history album</li>
                  <li>Submits chapter photo to Hog Calls when requested</li>
                </ul>
              </p>
            <h2><a href="chapter-officers.php#membership" name="membership" style="color:#000;">Membership Officer</a></h2> 
              <p>
                <ul>
                  <li>Ensures that chapter members have current national H.O.G. memberships</li>
                  <li>Enrolls new members into chapter</li>
                  <li>Administers annual chapter member renewal process</li>
                  <li>Works with chapter secretary to ensure the annual Chapter Membership Enrollment Form and Release is on file for each member</li>
                  <li>Complete quarterly membership change report and send to H.O.G. office if not maintaining membership report in the Chapter Business site of www.hog.com.</li>
                </ul>
              </p>
            <h2><a href="chapter-officers.php#webmaster" name="webmaster" style="color:#000;">Webmaster</a></h2> 
              <p>
                <ul>
                  <li>Authors, edits and updates the chapter web site</li>
                  <li>Follows Internet guidelines in Section H of the Chapter Handbook</li>
                  <li>Presents any potential web site information to the sponsoring dealer for approval prior to use. If the sponsoring dealer has a home page, discuss linking the chapter home page to the dealer's</li>
                  <li>Includes official chapter name and number on all material published online by the chapter</li>
                  <li>Works with the chapter editor to ensure the same information is being communicated to all chapter members and does not rely on the Internet as sole means of chapter communication</li>
                  <li>Organizes and assembles electronic information for the chapter</li>
                  <li>Submits chapter suggestions to the Chapter Officer News®</li>
                  <li>Informs chapter members of chapter activities by including in the chapter web site</li>
                  <li>Promotes H.O.G.’s fun, family-oriented philosophy in all web site material</li>
                  <li>Acts as caretaker for chapter by protecting chapter members privacy by not revealing personal chapter member or private chapter information on the chapter web site. </li>
                  <li>Submits chapter web site to H.O.G. for listing on hog.com and the h-d dealer locator.</li>
                  <li>Maintains, updates and operates all social media accounts (ie: facebook, twitter, etc)</li>
                </ul>
              </p>
            <h2><a href="chapter-officers.php#historian" name="historian" style="color:#000;">Historian</a></h2> 
              <p>
                <ul>
                  <li>Organizes written accounts of chapter activities, records names of chapter officers, notes membership levels, etc.</li>
                  <li>Organizes photos of chapter activities, officers, members, etc.</li>
                </ul>
              </p>
            <h2><a href="chapter-officers.php#merchandise" name="merchandise" style="color:#000;">Quartermaster / Merchandise</a></h2> 
              <p>
                <ul>
                  <li>Manages the sales of all chapter merchandise; patches, pins, shirts, hats, etc.</li>
                  <li>Maintains inventory of all chapter merchandise.</li>
                  <li>Provides board with report of needed items to maintain inventory and suggests new items for possible sale by chapter.</li>
                  <li>Orders merchandise as instructed.</li>
                </ul>
              </p>
